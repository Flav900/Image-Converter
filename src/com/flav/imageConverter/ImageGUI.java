    public void convertBmpToPng(File input) throws IOException {
        //Create file for the source  
//        File input = new File(filename);

        //Read the file to a BufferedImage  
        BufferedImage image = ImageIO.read(input);
        // Create a file for the output  
        File output = new File(input.getParentFile().getAbsolutePath() + "/png/" + input.getName().substring(0, input.getName().lastIndexOf(".")) + ".png");
        output.getParentFile().mkdirs();
//Write the image to the destination as a PNG  
        ImageIO.write(image, "png", output);
    }

