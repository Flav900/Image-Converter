    private void convertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertBtnActionPerformed
        new Thread(() -> {
            File destinationFiles = new File(destinationTextArea.getText());
            File[] paths;

            // returns pathnames for files and directory
            paths = destinationFiles.listFiles((File dir, String name1) -> name1.toLowerCase().endsWith(".bmp"));

            // for each pathname in pathname array
            for (int i = 0; i < paths.length; i++) {

                statusLbl.setText((i + 1) + " of " + paths.length);
                try {
                    if (paths[i].isFile()) {                       
                            convertBmpToPng(paths[i]);                        
                    }
                } catch (Exception ex) {
                    Logger.getLogger(ImageGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            JOptionPane.showMessageDialog(this, "Done!");
            statusLbl.setText("Ready");
        }).start();
    }//GEN-LAST:event_convertBtnActionPerformed

